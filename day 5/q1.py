import math

print(f"The value of pi is: {math.pi}")

num = 25
print(f"The square root of {num} is: {math.sqrt(num)}")

print(f"The floor of 4.7 is: {math.floor(4.7)}")

a = 4 ** 3
print(f"4 to the power of 3 is: {a}")

print(f"4 to the power of 3 using math.pow is: {math.pow(4, 3)}")

x, y = 4, 1
print(f"The sum of {x} and {y} is: {x + y}")
p, q = 4, 1
print(f"The sub of {p} and {q} is: {p - q}")

